const { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } = require('node:fs');
const { spawn } = require('node:child_process');
const os = require('node:os');
const { dirname, join, relative, resolve } = require('node:path');
const { buildCodegenArgs, projectDefaults } = require('./run-codegen-options.cjs');

const TEMPLATE_SIGNATURE = 'Generated by run-codegen.cjs';

function defaultLoadRecordingConfig(kind, name) {
  if (!name) {
    return null;
  }

  const filePath = join(process.cwd(), 'config', 'recording', `${kind}s`, `${name}.json`);

  if (!existsSync(filePath)) {
    return null;
  }

  return JSON.parse(readFileSync(filePath, 'utf8'));
}

function resolveBrowserName(options = {}) {
  const defaults = projectDefaults(options.project);
  return options.browser ?? defaults.browser ?? 'chromium';
}

function buildRecordingContextOptions(options = {}, deps = {}) {
  const devices = deps.devices ?? {};
  const contextOptions = {
    viewport: { width: 1440, height: 900 },
    ignoreHTTPSErrors: true,
  };

  if (!options.device) {
    return contextOptions;
  }

  const deviceDescriptor = devices[options.device];

  if (!deviceDescriptor) {
    throw new Error(`Unknown device preset: ${options.device}`);
  }

  return {
    ...contextOptions,
    ...deviceDescriptor,
  };
}

function resolveBrowserType(playwright, browserName) {
  const browserType = playwright?.[browserName];

  if (!browserType || typeof browserType.launch !== 'function') {
    throw new Error(`Unsupported browser type: ${browserName}`);
  }

  return browserType;
}

function resolveRecordingRuntime(options = {}, deps = {}) {
  const loadRecordingConfig = deps.loadRecordingConfig ?? defaultLoadRecordingConfig;
  const contextOptions = buildRecordingContextOptions(options, deps);
  const envConfig = options.env ? loadRecordingConfig('env', options.env) ?? {} : {};
  const roleConfig = options.role ? loadRecordingConfig('role', options.role) ?? {} : {};
  const headers = {
    ...(envConfig.headers ?? {}),
    ...(roleConfig.headers ?? {}),
    ...(options.headers ?? {}),
  };

  if (envConfig.baseURL) {
    contextOptions.baseURL = envConfig.baseURL;
  }

  if (Object.keys(headers).length > 0) {
    contextOptions.extraHTTPHeaders = headers;
  }

  if (roleConfig.storageState) {
    contextOptions.storageState = roleConfig.storageState;
  }

  return {
    url: options.url,
    contextOptions,
  };
}

function toPosixPath(value) {
  return value.replaceAll('\\', '/');
}

function toImportPath(fromFilePath, targetFilePath) {
  const importPath = toPosixPath(
    relative(dirname(resolve(process.cwd(), fromFilePath)), resolve(process.cwd(), targetFilePath)),
  ).replace(/\.[^.]+$/, '');

  if (!importPath.startsWith('.')) {
    return `./${importPath}`;
  }

  return importPath;
}

function toTitleCase(value = '') {
  return value
    .replace(/[-_]+/g, ' ')
    .trim()
    .replace(/\s+/g, ' ')
    .replace(/\b\w/g, char => char.toUpperCase());
}

function buildScenarioTitle(name) {
  const title = toTitleCase(name ?? '');
  return title || 'Recorded Scenario';
}

function buildScenarioTestTitle() {
  return 'executes recorded flow';
}

function stripUndefinedKeys(value) {
  if (Array.isArray(value)) {
    return value.map(stripUndefinedKeys);
  }

  if (!value || typeof value !== 'object') {
    return value;
  }

  return Object.fromEntries(
    Object.entries(value)
      .filter(([, nestedValue]) => nestedValue !== undefined)
      .map(([key, nestedValue]) => [key, stripUndefinedKeys(nestedValue)]),
  );
}

function buildGroupTestOptions(options = {}, runtime = {}) {
  const contextOptions = { ...(runtime.contextOptions ?? {}) };
  const baseURL = contextOptions.baseURL;
  const headers = contextOptions.extraHTTPHeaders;

  delete contextOptions.baseURL;
  delete contextOptions.extraHTTPHeaders;

  return stripUndefinedKeys({
    baseURL,
    initialURL: runtime.url ?? undefined,
    headers: headers && Object.keys(headers).length > 0 ? headers : undefined,
    contextOptions: Object.keys(contextOptions).length > 0 ? contextOptions : undefined,
  });
}

function renderCodegenTemplate(options = {}, runtime = {}, deps = {}) {
  const outputPath = options.output;
  const groupContextImportPath = toImportPath(outputPath, join(process.cwd(), 'utils', 'groupContext.ts'));
  const allureImportPath = toImportPath(outputPath, join(process.cwd(), 'utils', 'allure.ts'));
  const title = buildScenarioTitle(options.name);
  const recordedBody = deps.recordedBody?.trim() ?? '';
  const groupTestOptions = JSON.stringify(buildGroupTestOptions(options, runtime), null, 2)
    .split('\n')
    .map((line, index) => (index === 0 ? line : `  ${line}`))
    .join('\n');
  const body = recordedBody
    ? recordedBody
        .split('\n')
        .map(line => `    ${line}`)
        .join('\n')
    : '    // Paste recorded steps here.';

  return `import { expect } from '${allureImportPath}';
import { createGroupTest } from '${groupContextImportPath}';

// ${TEMPLATE_SIGNATURE}
const test = createGroupTest(${groupTestOptions});

test.describe('${title}', () => {
  test('${buildScenarioTestTitle()}', async ({ page }) => {
${body}
  });
});
`;
}

function isManagedTemplate(content = '') {
  return content.includes(TEMPLATE_SIGNATURE);
}

function ensureCodegenTemplate(options = {}, runtime = {}) {
  const outputPath = options.output;

  if (!outputPath) {
    return;
  }

  if (existsSync(outputPath)) {
    const currentContent = readFileSync(outputPath, 'utf8');

    if (!isManagedTemplate(currentContent)) {
      return;
    }
  }

  writeFileSync(outputPath, renderCodegenTemplate(options, runtime), 'utf8');
}

function resolveCodegenUrl(runtime = {}) {
  const baseURL = runtime.contextOptions?.baseURL;

  if (!runtime.url) {
    return baseURL ?? null;
  }

  if (!baseURL) {
    return runtime.url;
  }

  try {
    return new URL(runtime.url, baseURL).toString();
  } catch {
    return runtime.url;
  }
}

function buildNativeCodegenArgs(options = {}, runtime = {}, tempOutputPath, tempStoragePath) {
  const args = buildCodegenArgs({
    ...options,
    url: resolveCodegenUrl(runtime),
    output: tempOutputPath,
  });

  if (tempStoragePath) {
    args.push('--load-storage', tempStoragePath);
  }

  return args;
}

function createTempStorageState(runtime = {}) {
  const storageState = runtime.contextOptions?.storageState;

  if (!storageState) {
    return null;
  }

  const tempDir = mkdtempSync(join(os.tmpdir(), 'run-codegen-storage-'));
  const tempStoragePath = join(tempDir, 'storage-state.json');

  writeFileSync(tempStoragePath, JSON.stringify(storageState, null, 2), 'utf8');
  return {
    tempDir,
    tempStoragePath,
  };
}

function extractGeneratedTestBody(source = '') {
  const match = source.match(/test\([^]*?async\s*\(\{\s*page\s*\}\)\s*=>\s*\{([\s\S]*?)\n\}\);?\s*$/);

  if (!match) {
    return '';
  }

  return match[1]
    .split('\n')
    .map(line => line.replace(/^ {2}/, ''))
    .join('\n')
    .trim();
}

function normalizeRecordedBody(source = '', runtime = {}) {
  const resolvedUrl = resolveCodegenUrl(runtime);
  let body = extractGeneratedTestBody(source);

  if (!body) {
    return '';
  }

  if (resolvedUrl) {
    const escapedUrl = resolvedUrl.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    body = body.replace(new RegExp(`^await page\\.goto\\('${escapedUrl}'\\);\\n?`), '');
    body = body.replace(new RegExp(`^await page\\.goto\\("${escapedUrl}"\\);\\n?`), '');
  }

  return body
    .split('\n')
    .map(line => line.replace(/\s+$/g, ''))
    .join('\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}

function runNativeCodegen(codegenArgs = []) {
  return new Promise((resolvePromise, rejectPromise) => {
    const cliPath = require.resolve('@playwright/test/cli');
    const child = spawn(process.execPath, [cliPath, ...codegenArgs.slice(1)], {
      cwd: process.cwd(),
      stdio: 'inherit',
    });

    child.on('error', rejectPromise);
    child.on('exit', code => {
      if (code === 0) {
        resolvePromise();
        return;
      }

      rejectPromise(new Error(`playwright codegen exited with code ${code}`));
    });
  });
}

async function runCodegen(deps = {}) {
  const options = deps.options ?? {};
  const devices = deps.devices ?? deps.playwright?.devices ?? {};
  const loadRecordingConfig = deps.loadRecordingConfig ?? defaultLoadRecordingConfig;
  const runtime = resolveRecordingRuntime(options, {
    devices,
    loadRecordingConfig,
  });
  ensureCodegenTemplate(options, runtime);
  let tempOutputDir = null;
  let tempStorageDir = null;

  try {
    tempOutputDir = mkdtempSync(join(os.tmpdir(), 'run-codegen-output-'));
    const tempOutputPath = join(tempOutputDir, 'recorded.spec.ts');
    const tempStorage = createTempStorageState(runtime);
    tempStorageDir = tempStorage?.tempDir ?? null;
    const nativeCodegenArgs = buildNativeCodegenArgs(
      options,
      runtime,
      tempOutputPath,
      tempStorage?.tempStoragePath,
    );

    if (runtime.contextOptions?.extraHTTPHeaders) {
      console.warn(
        '[run-codegen] Native codegen does not support extraHTTPHeaders during recording. Headers are still written into the generated createGroupTest template.',
      );
    }

    await runNativeCodegen(nativeCodegenArgs);

    if (existsSync(tempOutputPath)) {
      const generatedSource = readFileSync(tempOutputPath, 'utf8');
      const recordedBody = normalizeRecordedBody(generatedSource, runtime);
      writeFileSync(options.output, renderCodegenTemplate(options, runtime, { recordedBody }), 'utf8');
    }
  } finally {
    if (tempOutputDir) {
      rmSync(tempOutputDir, { force: true, recursive: true });
    }

    if (tempStorageDir) {
      rmSync(tempStorageDir, { force: true, recursive: true });
    }
  }
}

